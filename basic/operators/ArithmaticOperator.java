package basic.operators;

public class ArithmaticOperator {
    public static void main(String[] args) {
        int a=10,b=3;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
