package com.basic.operator.assignment;

public class BasiAssignment {
    public static void main(String[] args) {
        int a=11,b=2;
        System.out.println(a+=b); //a=a+b
        System.out.println(a-=b); //a=a-b
        System.out.println(a*=b); //a=a*b
        System.out.println(a/=b); //a=a/b
        System.out.println(a%=b); //a=a%b

    }
}
