package com.basic;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a=10,b=30;
		System.out.println(a+=b); //a=a+b
		System.out.println(a-=b); //a=a+b
		System.out.println(a*=b); //a=a+b
		System.out.println(a/=b); //a=a+b
		System.out.println(a%=b); //a=a+b

	}

}
