package com.basic;

public class RelationalOperator {

	public static void main(String[] args) {
		int a=40,b=50;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}

}
