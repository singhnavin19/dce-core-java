package com.oops.mathExample;

public class MathBasic {
	public static void main(String[] args) {
		System.out.println("hello");
		System.out.println((int)Math.pow(5, 3));
		System.out.println(Math.max(15, 8));
		
		System.out.println(Math.sqrt(25));
		
		System.out.println(Math.abs(-34.89));
		System.out.println(Math.abs(Math.round(-34.89)));
		System.out.println(Math.ceil(34.01));
		System.out.println(Math.floor(34.89));
		
		
	}
}
